# age.py

# Ask the user for their current age
current_age = int(input("Enter your age: "))

# Calculate future ages
age_in_10_years = current_age + 10
age_in_20_years = current_age + 20
age_in_30_years = current_age + 30
age_in_40_years = current_age + 40

# Output the results
print(f"In 10 years, you will be {age_in_10_years} years old.")
print(f"In 20 years, you will be {age_in_20_years} years old.")
print(f"In 30 years, you will be {age_in_30_years} years old.")
print(f"In 40 years, you will be {age_in_40_years} years old.")
